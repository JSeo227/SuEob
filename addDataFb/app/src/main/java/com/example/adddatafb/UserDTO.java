package com.example.adddatafb;

public class UserDTO {
    // getter & setter

    String id, schoolName, eMail;
    int pw;

    public String getId() {
        return id;
    }
    public void setId(String asd) {
        id = asd;
    }

    public String getSchoolName() { return schoolName; }
    public void setSchoolName(String asd) { schoolName = asd; }

    public String getEMail() {
        return eMail;
    }
    public void setEMail(String asd) {
        eMail = asd;
    }

    public int getPw() {
        return pw;
    }
    public void setPw(int asd) {
        pw = asd;
    }
}
